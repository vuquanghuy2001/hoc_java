package ss4;

public class ex8 {
    public static void main(String[] args) {
        String day="monday";

        switch (day){
            case "sunday":
                System.out.println("First day of the week");
                break;
            case "monday":
                System.out.println("second day of the week");
                break;
            case "tuesday":
                System.out.println("second day of the week");
                break;
            case "wednesday":
                System.out.println("second day of the week");
                break;
            case "thursday":
                System.out.println("second day of the week");
                break;
            case "friday":
                System.out.println("second day of the week");
                break;
            case "saturday":
                System.out.println("second day of the week");
                break;
            default:
                System.out.println("invalid day");
        }
    }
}
